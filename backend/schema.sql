-- ==========================================
-- Database Schema for Vroomly (Supabase/PostgreSQL)
-- Matches database schema diagram
-- ==========================================

-- Enable Row Level Security (RLS) helpers or extensions if needed
-- (Supabase default configuration includes public schema access)

-- 1. Create categories table
CREATE TABLE IF NOT EXISTS categories (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE
);

-- 2. Create makes table
CREATE TABLE IF NOT EXISTS makes (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id INTEGER NOT NULL REFERENCES categories(id) ON DELETE CASCADE,
    name VARCHAR(255) NOT NULL
);

-- 3. Create models table
CREATE TABLE IF NOT EXISTS models (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    make_id INTEGER NOT NULL REFERENCES makes(id) ON DELETE CASCADE,
    name VARCHAR(255) NOT NULL
);

-- 4. Create users table
CREATE TABLE IF NOT EXISTS users (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    role VARCHAR(50) NOT NULL DEFAULT 'user',
    phone_number VARCHAR(50),
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 5. Create listings table
CREATE TABLE IF NOT EXISTS listings (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    category_id INTEGER NOT NULL REFERENCES categories(id) ON DELETE RESTRICT,
    make_id INTEGER NOT NULL REFERENCES makes(id) ON DELETE RESTRICT,
    model_id INTEGER NOT NULL REFERENCES models(id) ON DELETE RESTRICT,
    title VARCHAR(255) NOT NULL,
    description TEXT NOT NULL,
    price NUMERIC(12, 2) NOT NULL,
    production_year INTEGER NOT NULL,
    mileage INTEGER NOT NULL,
    fuel_type VARCHAR(50),
    engine_capacity INTEGER NOT NULL,
    engine_power INTEGER NOT NULL,
    gearbox VARCHAR(50) NOT NULL,
    body_type VARCHAR(50) NOT NULL,
    color VARCHAR(50) NOT NULL,
    country_of_origin VARCHAR(100) NOT NULL,
    steering_wheel_side VARCHAR(50),
    condition VARCHAR(50) NOT NULL,
    location VARCHAR(100) NOT NULL,
    status VARCHAR(50) DEFAULT 'active',
    is_featured BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 6. Create images table
CREATE TABLE IF NOT EXISTS images (
    id GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    listing_id INTEGER NOT NULL REFERENCES listings(id) ON DELETE CASCADE,
    image_url VARCHAR(1000) NOT NULL,
    is_main BOOLEAN DEFAULT FALSE
);

